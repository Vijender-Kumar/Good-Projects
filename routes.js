const express = require("express");
const mongoose = require("mongoose");
const userModel = require("./models").default;
const app = express();
app.post("/webhook-subscription", async (request, response) => {
    const user = new userModel(request.body);
  
    try {
      await user.save();
      response.send(user);
    } catch (error) {
      response.status(500).send(error);
    }
});

app.get("/get-sync-id", async (request, response) => {
    const users = await userModel.find({});
  
    try {
      response.send(users);
    } catch (error) {
      response.status(500).send(error);
    }
  });

  module.exports = mongoose.model('User', userModel);